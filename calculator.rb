
class Calculator
	def Calculator.add(int1, int2)
		result = int1 + int2

	end

	def Calculator.subtract(int1, int2)
		result = int1 - int2
	end

	def Calculator.multiply(int1, int2)
		result = int1 * int2
	end

	def Calculator.divide(int1, int2)
		result = int1 / int2
	end

	def Calculator.exponent(int1, int2)
		result = int1 ** int2
	end

	def Calculator.sq_root(int)
		result = Math.sqrt(int)
	end

end